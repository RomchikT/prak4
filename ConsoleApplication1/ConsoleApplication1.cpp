#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "Russian");
    const int ryd = 3;
    const int colon = 5;
    double matrix[ryd][colon];

    for (int i = 0; i < ryd; i++) {
        cout << "Введи значения для строчки " << (i + 1) << ": ";
        for (int j = 0; j < colon; j++) {
            cin >> matrix[i][j];
        }
    }

    double averages[ryd];
    for (int i = 0; i < ryd; i++) {
        double gg = 0;
        for (int j = 0; j < colon; j++) {
            gg += matrix[i][j];
        }
        averages[i] = gg / colon;
    }

    cout << "Среднее ариф для каждой строчки:" << endl;
    for (int i = 0; i < ryd; i++) {
        cout << "Строчка " << (i + 1) << ": " << averages[i] << endl;
    }

    return 0;
}
